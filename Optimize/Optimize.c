const int8_t kernel[3][3] = { 
    {1,0,-1},
    {2,0,-2},
    {1,0,-1}
};
int32_t output [height-2][width-2];


static void Conv2d(){

    const int out_height = height - 2;
    const int out_width = width - 2;


    // Compute the convolution
    for (int till_y = 0; till_y < out_height; till_y += till_height){
        for (int till_x = 0; till_x < out_width; till_x += till_width){

            int y_end = till_height;
            if (till_y + y_end > out_height) y_end = out_height - till_y;

            int x_end = till_width;
            if (till_x + x_end > out_width) x_end = out_width - till_x;

            for(int y = 0; y < y_end; y += 1){
                for(int x = 0; x < x_end; x += 1){

                    int32_t sum = 0;
                    for(int ky = 0; ky < kernel_height; ky++){
                        for(int kx = 0; kx < kernel_width; kx++){
                            sum += (int32_t)kernel[ky][kx] * (int32_t)image[till_y+y+ky][till_x+x+kx]; 
                        }
                    }
                    output[till_y+y][till_x+x] = sum;
                }
            }
        }
    }

    return;
}



